// function declaration
function soma(x, y) {
    return x + y
}
console.log(soma(3, 4))

// function expression (função anonima)
const sub = function (x, y) {
    return x - y
}
console.log(sub(2, 2))

// named function expression (pouco usada)
const mult = function mult (x, y) {
    return x * y
}
console.log(mult(2, 2))